@dataclass(frozen=True)
class NuclearVariantTarget:
    """Target nuclide for a generated prediction candidate."""

    nuclide_id: str
    Z: int
    N: int
    A: int

    @classmethod
    def from_mapping(cls, payload: dict[str, Any]) -> "NuclearVariantTarget":
        z = int(payload["Z"])
        n = int(payload["N"])
        a = int(payload.get("A", z + n))
        if z + n != a:
            raise ValueError(
                f"Target {payload.get('nuclide_id', '<unknown>')} has inconsistent Z + N != A"
            )
        return cls(
            nuclide_id=str(payload["nuclide_id"]),
            Z=z,
            N=n,
            A=a,
        )


@dataclass(frozen=True)
class NuclearTargetBatchLibraryIssue:
    """Review issue raised by reusable nuclear factory target-batch validation."""

    severity: str
    code: str
    message: str
    batch_id: str | None = None
    nuclide_id: str | None = None


@dataclass(frozen=True)
class NuclearPredictionCandidate:
    """Generated pre-reveal prediction candidate."""

    prediction_id: str | None
    variant_id: str
    model_id: str
    target_batch: str
    title: str
    coefficients: SemiEmpiricalCoefficients
    coefficient_delta: dict[str, float]
    transform: dict[str, Any]
    feature_terms: tuple[dict[str, Any], ...]
    target_rows: tuple[dict[str, Any], ...]
    model_control_label: str
    review_notes: tuple[str, ...]
    limitations: tuple[str, ...]

    def to_summary_row(self) -> dict[str, Any]:
        """Return a YAML-serializable slate summary row."""
        return {
            "prediction_id": self.prediction_id,
            "variant_id": self.variant_id,
            "model_id": self.model_id,
            "target_batch": self.target_batch,
            "title": self.title,
            "model_control_label": self.model_control_label,
            "coefficients": self.coefficients.to_dict(),
            "coefficient_delta_from_base": self.coefficient_delta,
            "transform": self.transform,
            "feature_terms": [dict(term) for term in self.feature_terms],
            "feature_term_summary": _feature_term_summary(self.target_rows),
            "target_nuclides": list(self.target_rows),
            "review_notes": list(self.review_notes),
            "limitations": list(self.limitations),
        }


def repository_root() -> Path:
    """Return the repository root containing this package."""
    return Path(__file__).resolve().parents[2]


def load_variant_factory_config(path: str | Path) -> dict[str, Any]:
    """Load a nuclear prediction variant factory YAML config."""
    config_path = Path(path)
    with config_path.open("r", encoding="utf-8") as handle:
        payload = yaml.safe_load(handle)
    if not isinstance(payload, dict):
        raise ValueError(f"Expected mapping in variant factory config: {config_path}")
    return payload


def load_target_batch_library(
    path: str | Path = DEFAULT_TARGET_BATCH_LIBRARY_PATH,
    *,
    repo_root: str | Path | None = None,
) -> dict[str, Any]:
    """Load and validate a reusable nuclear factory target-batch library."""
    root = Path(repo_root) if repo_root is not None else repository_root()
    library_path = Path(path)
    if not library_path.is_absolute():
        library_path = root / library_path
    with library_path.open("r", encoding="utf-8") as handle:
        payload = yaml.safe_load(handle)
    if not isinstance(payload, dict):
        raise ValueError(f"Expected mapping in target-batch library: {library_path}")
    issues = collect_target_batch_library_issues(payload, repo_root=root)
    blocking = [issue for issue in issues if issue.severity == "ERROR"]
    if blocking:
        details = "; ".join(issue.message for issue in blocking)
        raise ValueError(f"Invalid target-batch library {library_path}: {details}")
    return payload


def collect_target_batch_library_issues(
    library: dict[str, Any],
    *,
    repo_root: str | Path | None = None,
) -> tuple[NuclearTargetBatchLibraryIssue, ...]:
    """Collect reusable target-batch consistency and leakage-guard issues."""
    root = Path(repo_root) if repo_root is not None else repository_root()
    issues: list[NuclearTargetBatchLibraryIssue] = []
    source_guardrails = _mapping(library.get("source_guardrails", {}), "source_guardrails")
    if source_guardrails.get("live_external_fetch_allowed") is not False:
        issues.append(
            NuclearTargetBatchLibraryIssue(
                severity="ERROR",
                code="live_external_fetch_allowed",
                message="Target-batch library must keep live_external_fetch_allowed false.",
            )
        )

    committed_ids = _committed_guard_nuclide_ids(source_guardrails, root)
    raw_batches = _mapping(library.get("batches"), "batches")
    if not raw_batches:
        issues.append(
            NuclearTargetBatchLibraryIssue(
                severity="ERROR",
                code="missing_batches",
                message="Target-batch library must define at least one batch.",
            )
        )
        return tuple(issues)

    for batch_id, raw_batch in raw_batches.items():
        batch = _mapping(raw_batch, f"batches.{batch_id}")
        targets = _sequence(batch.get("targets"), f"batches.{batch_id}.targets")
        if not targets:
            issues.append(
                NuclearTargetBatchLibraryIssue(
                    severity="ERROR",
                    code="empty_target_batch",
                    message=f"Target batch {batch_id} must contain at least one target.",
                    batch_id=str(batch_id),
                )
            )
            continue

        seen_ids: set[str] = set()
        retrospective_control = bool(batch.get("retrospective_control", False))
        for raw_target in targets:
            try:
                target = NuclearVariantTarget.from_mapping(
                    _mapping(raw_target, f"batches.{batch_id}.targets")
                )
            except (KeyError, TypeError, ValueError) as exc:
                issues.append(
                    NuclearTargetBatchLibraryIssue(
                        severity="ERROR",
                        code="invalid_target",
                        message=f"Target batch {batch_id} has invalid target: {exc}",
                        batch_id=str(batch_id),
                    )
                )
                continue

            if target.nuclide_id in seen_ids:
                issues.append(
                    NuclearTargetBatchLibraryIssue(
                        severity="ERROR",
                        code="duplicate_nuclide_id",
                        message=f"Target batch {batch_id} repeats {target.nuclide_id}.",
                        batch_id=str(batch_id),
                        nuclide_id=target.nuclide_id,
                    )
                )
            seen_ids.add(target.nuclide_id)

            if target.nuclide_id in committed_ids and not retrospective_control:
                issues.append(
                    NuclearTargetBatchLibraryIssue(
                        severity="ERROR",
                        code="committed_measurement_target",
                        message=(
                            f"Target batch {batch_id} includes committed measured/holdout "
                            f"nuclide {target.nuclide_id} without retrospective_control: true."
                        ),
                        batch_id=str(batch_id),
                        nuclide_id=target.nuclide_id,
                    )
                )

    return tuple(issues)


def generate_variant_slate(
    config: dict[str, Any],
    *,
    repo_root: str | Path | None = None,
    output_dir: str | Path | None = None,
    write_drafts: bool | None = None,
) -> dict[str, Any]:
    """Generate a deterministic candidate slate from a factory config.

    Draft PRED files are written only when ``write_drafts`` is true or the config
    output block sets ``write_draft_predictions: true``. Even then, the caller
    must provide an explicit output directory through either the argument or the
    config.
    """
    root = Path(repo_root) if repo_root is not None else repository_root()
    output_config = _mapping(config.get("output", {}), "output")
    should_write = bool(output_config.get("write_draft_predictions", False))
    if write_drafts is not None:
        should_write = write_drafts

    _validate_source_state(config)
    _validate_quantity(config)
    _validate_unique_prediction_ids(config)

    base_coefficients = _load_base_coefficients(config, repo_root=root)
    targets_by_batch = _parse_target_batches(config, repo_root=root)
    candidates = tuple(
        _build_candidate(
            variant_config=variant_config,
            config=config,
            base_coefficients=base_coefficients,
            targets_by_batch=targets_by_batch,
        )
        for variant_config in _sequence(config.get("variants"), "variants")
    )

    summary = {
        "factory_id": str(config.get("factory_id", "nuclear-prediction-variant-factory")),
        "task_id": str(config.get("task_id", "TASK-0249")),
        "campaign_profile_id": str(config.get("campaign_profile_id", "nuclear-mass-surface")),
        "quantity": "mass_excess_mev",
        "unit": "MeV",
        "live_external_fetch_allowed": False,
        "candidate_count": len(candidates),
        "candidates": [candidate.to_summary_row() for candidate in candidates],
    }

    if should_write:
        draft_dir = _resolve_output_dir(output_dir=output_dir, output_config=output_config)
        _validate_output_directory(draft_dir, root, output_config)
        written_files = _write_draft_predictions(
            candidates=candidates,
            config=config,
            output_dir=draft_dir,
        )
        summary["written_prediction_files"] = [str(path) for path in written_files]

    return summary


def generate_variant_slate_from_config(
    config_path: str | Path,
    *,
    repo_root: str | Path | None = None,
    output_dir: str | Path | None = None,
    write_drafts: bool | None = None,
) -> dict[str, Any]:
    """Load a config file and generate the candidate slate."""
    config = load_variant_factory_config(config_path)
    return generate_variant_slate(
        config,
        repo_root=repo_root,
        output_dir=output_dir,
        write_drafts=write_drafts,
    )


def _build_candidate(
    *,
    variant_config: dict[str, Any],
    config: dict[str, Any],
    base_coefficients: SemiEmpiricalCoefficients,
    targets_by_batch: dict[str, tuple[NuclearVariantTarget, ...]],
) -> NuclearPredictionCandidate:
    variant_id = str(variant_config["variant_id"])
    target_batch = str(variant_config["target_batch"])
    if target_batch not in targets_by_batch:
        raise ValueError(f"Variant {variant_id} references unknown target batch: {target_batch}")

    transform = _mapping(variant_config.get("transform", {}), f"{variant_id}.transform")
    coefficients = apply_coefficient_transform(base_coefficients, transform)
    coefficient_delta = {
        name: coefficients.to_dict()[name] - base_coefficients.to_dict()[name]
        for name in COEFFICIENT_NAMES
    }
    feature_terms = _normalize_feature_terms(
        transform.get("feature_terms", []),
        variant_id=variant_id,
    )
    model_id = str(
        variant_config.get("model_id")
        or f"{_baseline_model_config(config)['registry_model_id']}::{variant_id}"
    )
    target_rows = tuple(
        _predict_target_row(
            target=target,
            coefficients=coefficients,
            base_coefficients=base_coefficients,
            feature_terms=feature_terms,
            confidence_note=str(
                variant_config.get(
                    "confidence_note",
                    (
                        f"Point estimate from {model_id} for the {target_batch} target batch; "
                        "no statistical interval is claimed."
                    ),
                )
            ),
        )
        for target in targets_by_batch[target_batch]
    )
    return NuclearPredictionCandidate(
        prediction_id=(
            None if variant_config.get("prediction_id") is None else str(variant_config["prediction_id"])
        ),
        variant_id=variant_id,
        model_id=model_id,
        target_batch=target_batch,
        title=str(
            variant_config.get(
                "title",
                f"Draft nuclear mass variant: {variant_id} on {target_batch}",
            )
        ),
        coefficients=coefficients,
        coefficient_delta=coefficient_delta,
        transform=transform,
        feature_terms=feature_terms,
        target_rows=target_rows,
        model_control_label=str(variant_config.get("model_control_label", "semi-empirical control")),
        review_notes=tuple(str(note) for note in variant_config.get("review_notes", [])),
        limitations=tuple(str(note) for note in variant_config.get("limitations", [])),
    )


def apply_coefficient_transform(
    coefficients: SemiEmpiricalCoefficients,
    transform: dict[str, Any],
) -> SemiEmpiricalCoefficients:
    """Apply a bounded coefficient transform to a semi-empirical coefficient set."""
    values = coefficients.to_dict()

    if "coefficients" in transform:
        explicit = _mapping(transform["coefficients"], "transform.coefficients")
        for name, value in explicit.items():
            _validate_coefficient_name(name)
            values[name] = float(value)

    if "blend_with_reference" in transform:
        weight = float(transform["blend_with_reference"])
        if not 0.0 <= weight <= 1.0:
            raise ValueError("blend_with_reference must be between 0.0 and 1.0")
        reference = REFERENCE_SEMI_EMPIRICAL_COEFFICIENTS.to_dict()
        values = {
            name: ((1.0 - weight) * values[name]) + (weight * reference[name])
            for name in COEFFICIENT_NAMES
        }

    for block_name, operator in (
        ("scale", lambda current, value: current * float(value)),
        ("delta", lambda current, value: current + float(value)),
        ("set", lambda _current, value: float(value)),
    ):
        if block_name not in transform:
            continue
        block = _mapping(transform[block_name], f"transform.{block_name}")
        for name, value in block.items():
            _validate_coefficient_name(name)
            values[name] = operator(values[name], value)

    return SemiEmpiricalCoefficients(
        volume=float(values["volume"]),
        surface=float(values["surface"]),
        coulomb=float(values["coulomb"]),
        asymmetry=float(values["asymmetry"]),
        pairing=float(values["pairing"]),
    )


def feature_term_correction_mev(
    *,
    z: int,
    n: int,
    feature_terms: tuple[dict[str, Any], ...] | list[dict[str, Any]],
) -> float:
    """Sum the bounded feature-term corrections for one nuclide in MeV.

    Returns 0.0 when ``feature_terms`` is empty. Each entry must be a normalized
    mapping produced by :func:`_normalize_feature_terms`.
    """
    total = 0.0
    for term in feature_terms:
        total += _evaluate_feature_term(term, z=z, n=n)
    return total


def _evaluate_feature_term(term: dict[str, Any], *, z: int, n: int) -> float:
    term_type = term["type"]
    if term_type == "shell_proximity_gaussian":
        axis = term["axis"]
        coefficient = float(term["coefficient"])
        sigma = float(term["sigma"])
        magic_numbers = tuple(int(x) for x in term["magic_numbers"])
        axis_value = z if axis == "z" else n
        distance = min(abs(axis_value - magic) for magic in magic_numbers)
        return coefficient * math.exp(-(distance ** 2) / (2.0 * sigma ** 2))
    if term_type == "asymmetry_polynomial":
        a = float(z + n)
        isospin = (n - z) / a
        contribution = 0.0
        for sub in term["terms"]:
            contribution += float(sub["coefficient"]) * (isospin ** int(sub["power"]))
        return contribution
    if term_type == "asymmetric_neutron_excess":
        a = float(z + n)
        excess = max(n - z, 0)
        power = int(term["power"])
        return float(term["coefficient"]) * (excess ** power) / a
    raise ValueError(f"Unsupported feature term type: {term_type}")


def _normalize_feature_terms(
    feature_terms: Any,
    *,
    variant_id: str,
) -> tuple[dict[str, Any], ...]:
    if feature_terms is None:
        return tuple()
    if not isinstance(feature_terms, list):
        raise ValueError(f"{variant_id}.transform.feature_terms must be a list")
    normalized: list[dict[str, Any]] = []
    for index, raw in enumerate(feature_terms):
        location = f"{variant_id}.transform.feature_terms[{index}]"
        term_payload = _mapping(raw, location)
        term_type = str(term_payload.get("type", ""))
        if term_type not in FEATURE_TERM_TYPES:
            raise ValueError(
                f"{location}: unknown feature term type {term_type!r}; "
                f"allowed types: {', '.join(FEATURE_TERM_TYPES)}"
            )
        if term_type == "shell_proximity_gaussian":
            normalized.append(_normalize_shell_proximity_gaussian(term_payload, location))
        elif term_type == "asymmetry_polynomial":
            normalized.append(_normalize_asymmetry_polynomial(term_payload, location))
        elif term_type == "asymmetric_neutron_excess":
            normalized.append(_normalize_asymmetric_neutron_excess(term_payload, location))
    return tuple(normalized)


def _normalize_shell_proximity_gaussian(payload: dict[str, Any], location: str) -> dict[str, Any]:
    axis = str(payload.get("axis", "")).lower()
    if axis not in ("z", "n"):
        raise ValueError(f"{location}: shell_proximity_gaussian axis must be 'z' or 'n'")
    if "coefficient" not in payload:
        raise ValueError(f"{location}: shell_proximity_gaussian requires coefficient")
    sigma = float(payload.get("sigma", DEFAULT_SHELL_PROXIMITY_SIGMA))
    if sigma <= 0:
        raise ValueError(f"{location}: shell_proximity_gaussian sigma must be > 0")
    magic_numbers = payload.get("magic_numbers", MAGIC_NUMBERS)
    if not isinstance(magic_numbers, (list, tuple)) or not magic_numbers:
        raise ValueError(f"{location}: shell_proximity_gaussian magic_numbers must be a non-empty list")
    magic_ints = tuple(int(x) for x in magic_numbers)
    if any(value < 0 for value in magic_ints):
        raise ValueError(f"{location}: magic_numbers entries must be non-negative")
    return {
        "type": "shell_proximity_gaussian",
        "axis": axis,
        "coefficient": float(payload["coefficient"]),
        "sigma": sigma,
        "magic_numbers": list(magic_ints),
    }


def _normalize_asymmetry_polynomial(payload: dict[str, Any], location: str) -> dict[str, Any]:
    raw_terms = payload.get("terms")
    if not isinstance(raw_terms, list) or not raw_terms:
        raise ValueError(f"{location}: asymmetry_polynomial requires non-empty terms list")
    normalized_terms: list[dict[str, Any]] = []
    seen_powers: set[int] = set()
    for sub_index, sub in enumerate(raw_terms):
        sub_location = f"{location}.terms[{sub_index}]"
        sub_map = _mapping(sub, sub_location)
        if "power" not in sub_map or "coefficient" not in sub_map:
            raise ValueError(f"{sub_location}: must include power and coefficient")
        power = int(sub_map["power"])
        if power < 1 or power > MAX_ASYMMETRY_POLYNOMIAL_POWER:
            raise ValueError(
                f"{sub_location}: power must be between 1 and "
                f"{MAX_ASYMMETRY_POLYNOMIAL_POWER}"
            )
        if power in seen_powers:
            raise ValueError(f"{sub_location}: duplicate power {power}")
        seen_powers.add(power)
        normalized_terms.append({"power": power, "coefficient": float(sub_map["coefficient"])})
    normalized_terms.sort(key=lambda t: t["power"])
    return {
        "type": "asymmetry_polynomial",
        "terms": normalized_terms,
    }


def _normalize_asymmetric_neutron_excess(payload: dict[str, Any], location: str) -> dict[str, Any]:
    if "coefficient" not in payload:
        raise ValueError(f"{location}: asymmetric_neutron_excess requires coefficient")
    power = int(payload.get("power", DEFAULT_ASYMMETRIC_NEUTRON_EXCESS_POWER))
    if power < 1:
        raise ValueError(f"{location}: asymmetric_neutron_excess power must be >= 1")
    return {
        "type": "asymmetric_neutron_excess",
        "coefficient": float(payload["coefficient"]),
        "power": power,
    }


def _feature_term_summary(target_rows: tuple[dict[str, Any], ...]) -> dict[str, Any]:
    corrections = [
        float(row.get("feature_term_correction_mev", 0.0))
        for row in target_rows
    ]
    if not corrections:
        return {"max_abs_correction_mev": 0.0, "mean_abs_correction_mev": 0.0}
    abs_values = [abs(value) for value in corrections]
    return {
        "max_abs_correction_mev": round(max(abs_values), 6),
        "mean_abs_correction_mev": round(sum(abs_values) / len(abs_values), 6),
    }


def _predict_target_row(
    *,
    target: NuclearVariantTarget,
    coefficients: SemiEmpiricalCoefficients,
    base_coefficients: SemiEmpiricalCoefficients,
    feature_terms: tuple[dict[str, Any], ...],
    confidence_note: str,
) -> dict[str, Any]:
    mass_excess_mev, feature_correction = _mass_excess_mev_with_features(
        target=target,
        coefficients=coefficients,
        feature_terms=feature_terms,
    )
    baseline_mass_excess_mev, _ = _mass_excess_mev_with_features(
        target=target,
        coefficients=base_coefficients,
        feature_terms=tuple(),
    )
    row: dict[str, Any] = {
        "nuclide_id": target.nuclide_id,
        "Z": target.Z,
        "N": target.N,
        "A": target.A,
        "pairing_class": pairing_class(target.Z, target.N),
        "predicted_value_mev": round(mass_excess_mev, 6),
        "baseline_value_mev": round(baseline_mass_excess_mev, 6),
        "delta_from_baseline_mev": round(mass_excess_mev - baseline_mass_excess_mev, 6),
        "uncertainty_mev": None,
        "confidence_note": confidence_note,
    }
    if feature_terms:
        row["feature_term_correction_mev"] = round(feature_correction, 6)
    return row


def _mass_excess_mev_with_features(
    *,
    target: NuclearVariantTarget,
    coefficients: SemiEmpiricalCoefficients,
    feature_terms: tuple[dict[str, Any], ...],
) -> tuple[float, float]:
    binding_energy = semi_empirical_binding_energy(
        z=target.Z,
        n=target.N,
        coefficients=coefficients,
    )
    feature_correction = feature_term_correction_mev(
        z=target.Z,
        n=target.N,
        feature_terms=feature_terms,
    )
    mass_defect_u = (binding_energy + feature_correction) / ATOMIC_MASS_UNIT_MEV
    atomic_mass_u = (
        target.Z * HYDROGEN_ATOM_MASS_U
        + target.N * NEUTRON_MASS_U
        - mass_defect_u
    )
    mass_excess_mev = mass_excess_keV_from_atomic_mass_u(
        a=target.A,
        atomic_mass_u=atomic_mass_u,
    ) / 1000.0
    return mass_excess_mev, feature_correction


def _write_draft_predictions(
    *,
    candidates: tuple[NuclearPredictionCandidate, ...],
    config: dict[str, Any],
    output_dir: Path,
) -> list[Path]:
    output_dir.mkdir(parents=True, exist_ok=True)
    paths = [_draft_prediction_path(candidate, output_dir) for candidate in candidates]
    _validate_unique_output_paths(paths)
    existing_paths = [path for path in paths if path.exists()]
    if existing_paths:
        joined = ", ".join(str(path) for path in existing_paths)
        raise FileExistsError(f"Refusing to overwrite existing draft prediction file(s): {joined}")

    written: list[Path] = []
    for candidate, path in zip(candidates, paths):
        payload = _candidate_to_prediction_entry(candidate, config)
        with path.open("w", encoding="utf-8") as handle:
            yaml.safe_dump(payload, handle, sort_keys=False, allow_unicode=False)
        written.append(path)
    return written


def _candidate_to_prediction_entry(
    candidate: NuclearPredictionCandidate,
    config: dict[str, Any],
) -> dict[str, Any]:
    if candidate.prediction_id is None:
        raise ValueError(f"Variant {candidate.variant_id} must set prediction_id to write draft PRED YAML")

    source_state = _mapping(config["source_state"], "source_state")
    baseline_model = _baseline_model_config(config)
    limitations = [
        "Factory-generated draft candidate only; not a claim or result package.",
        "Targets are selected against committed repository datasets only; no live external source was fetched during generation.",
        "The semi-empirical coefficient variant is a bounded review candidate and is not promoted as a validated nuclear-mass model.",
        "Only a maintainer-reviewed subset should be copied into the committed prediction registry before reveal.",
    ]
    limitations.extend(candidate.limitations)

    return {
        "prediction_id": candidate.prediction_id,
        "title": candidate.title,
        "registry_status": "REGISTERED",
        "campaign_profile_id": "nuclear-mass-surface",
        "task_id": str(config.get("task_id", "TASK-0249")),
        "evidence_class": "prospective_prediction_registry",
        "claim_ceiling": str(config.get("claim_ceiling", DEFAULT_CLAIM_CEILING)),
        "registered_by": dict(_mapping(config["registered_by"], "registered_by")),
        "registered_at_utc": str(config["registered_at_utc"]),
        "source_state": {
            "git_commit": str(source_state["git_commit"]),
            "model_reference": {
                "model_id": candidate.model_id,
                "source_path": str(baseline_model["source_path"]),
                "frozen_parameters_note": _frozen_parameters_note(candidate, baseline_model),
            },
            "baseline_reference": {
                "result_id": str(baseline_model.get("result_id", "RESULT-0015")),
                "source_path": str(baseline_model["source_path"]),
            },
            "training_data_references": list(source_state["training_data_references"]),
            "holdout_protocol_references": list(source_state["holdout_protocol_references"]),
            "live_external_fetch_allowed": False,
            "source_data_state_note": str(source_state["source_data_state_note"]),
        },
        "target_set": {
            "label": candidate.target_batch,
            "quantity": "mass_excess_mev",
            "unit": "MeV",
            "target_nuclides": [_target_row_for_registry(row) for row in candidate.target_rows],
        },
        "uncertainty_semantics": {
            "type": "point_estimate_only",
            "note": str(config.get("uncertainty_note", DEFAULT_UNCERTAINTY_NOTE)),
        },
        "reveal_conditions": dict(_mapping(config["reveal_conditions"], "reveal_conditions")),
        "limitations": limitations,
        "review_boundary": {
            "retrospective_equivalence_forbidden": True,
            "pre_reveal_claim_promotion_allowed": False,
            "post_reveal_claim_promotion_requires_review": True,
            "canonical_result_allowed_pre_reveal": False,
        },
    }


def _target_row_for_registry(row: dict[str, Any]) -> dict[str, Any]:
    return {
        "nuclide_id": row["nuclide_id"],
        "Z": row["Z"],
        "N": row["N"],
        "A": row["A"],
        "predicted_value_mev": row["predicted_value_mev"],
        "uncertainty_mev": row["uncertainty_mev"],
        "confidence_note": row["confidence_note"],
    }


def _frozen_parameters_note(
    candidate: NuclearPredictionCandidate,
    baseline_model: dict[str, Any],
) -> str:
    coefficient_text = ", ".join(
        f"{name}={value:.12g}" for name, value in candidate.coefficients.to_dict().items()
    )
    delta_text = ", ".join(
        f"{name}={value:+.12g}" for name, value in candidate.coefficient_delta.items()
    )
    source_note = str(baseline_model.get("frozen_parameters_note", "Loaded from baseline result."))
    return (
        f"{source_note} Variant {candidate.variant_id} freezes coefficients: "
        f"{coefficient_text}. Delta from base: {delta_text}."
    )


def _draft_prediction_path(candidate: NuclearPredictionCandidate, output_dir: Path) -> Path:
    if candidate.prediction_id is None:
        raise ValueError(f"Variant {candidate.variant_id} must set prediction_id to write draft PRED YAML")
    return output_dir / f"{candidate.prediction_id}.yaml"


def _load_base_coefficients(config: dict[str, Any], *, repo_root: Path) -> SemiEmpiricalCoefficients:
    baseline = _baseline_model_config(config)
    if "coefficients" in baseline:
        return _coefficients_from_mapping(_mapping(baseline["coefficients"], "baseline_model.coefficients"))

    result_path = repo_root / str(baseline["source_path"])
    score_model_id = str(baseline.get("score_model_id", baseline.get("model_id", "")))
    if not score_model_id:
        raise ValueError("baseline_model must set score_model_id when coefficients are loaded from a result")
    return _load_result_score_coefficients(result_path, score_model_id)


def _load_result_score_coefficients(
    result_path: Path,
    score_model_id: str,
) -> SemiEmpiricalCoefficients:
    with result_path.open("r", encoding="utf-8") as handle:
        result = yaml.safe_load(handle)
    if not isinstance(result, dict):
        raise ValueError(f"Expected mapping in result file: {result_path}")
    for score in result.get("scores", []):
        if not isinstance(score, dict):
            continue
        if str(score.get("model_id")) == score_model_id:
            return _coefficients_from_mapping(_mapping(score["coefficients"], f"{score_model_id}.coefficients"))
    raise ValueError(f"Could not find score model_id {score_model_id!r} in {result_path}")


def _coefficients_from_mapping(payload: dict[str, Any]) -> SemiEmpiricalCoefficients:
    missing = [name for name in COEFFICIENT_NAMES if name not in payload]
    if missing:
        raise ValueError(f"Coefficient mapping is missing: {', '.join(missing)}")
    return SemiEmpiricalCoefficients(
        volume=float(payload["volume"]),
        surface=float(payload["surface"]),
        coulomb=float(payload["coulomb"]),
        asymmetry=float(payload["asymmetry"]),
        pairing=float(payload["pairing"]),
    )


def _parse_target_batches(
    config: dict[str, Any],
    *,
    repo_root: Path,
) -> dict[str, tuple[NuclearVariantTarget, ...]]:
    raw_batches: dict[str, Any] = {}
    if "target_batch_library" in config:
        raw_batches.update(_target_batches_from_library(config, repo_root=repo_root))
    if "target_batches" in config:
        inline_batches = _mapping(config.get("target_batches"), "target_batches")
        duplicate_labels = sorted(set(raw_batches).intersection(str(label) for label in inline_batches))
        if duplicate_labels:
            raise ValueError(
                "target_batches duplicates target_batch_library labels: "
                + ", ".join(duplicate_labels)
            )
        raw_batches.update({str(label): targets for label, targets in inline_batches.items()})
    if not raw_batches:
        raise ValueError("Factory config must define target_batches or target_batch_library")

    batches: dict[str, tuple[NuclearVariantTarget, ...]] = {}
    for label, targets in raw_batches.items():
        target_rows = tuple(
            NuclearVariantTarget.from_mapping(_mapping(target, f"target_batches.{label}"))
            for target in _sequence(targets, f"target_batches.{label}")
        )
        if not target_rows:
            raise ValueError(f"Target batch {label} must contain at least one target")
        batches[str(label)] = target_rows
    return batches


def _target_batches_from_library(
    config: dict[str, Any],
    *,
    repo_root: Path,
) -> dict[str, Any]:
    library_config = _mapping(config.get("target_batch_library"), "target_batch_library")
    library_path = Path(str(library_config.get("path", DEFAULT_TARGET_BATCH_LIBRARY_PATH)))
    library = load_target_batch_library(library_path, repo_root=repo_root)
    library_batches = _mapping(library.get("batches"), "batches")
    include_batches = library_config.get("include_batches")
    if include_batches is None:
        selected_labels = [str(label) for label in library_batches]
    else:
        selected_labels = [str(label) for label in _sequence(include_batches, "target_batch_library.include_batches")]
    selected: dict[str, Any] = {}
    for label in selected_labels:
        if label not in library_batches:
            raise ValueError(f"target_batch_library references unknown batch: {label}")
        selected[label] = _sequence(
            _mapping(library_batches[label], f"batches.{label}").get("targets"),
            f"batches.{label}.targets",
        )
    return selected


def _committed_guard_nuclide_ids(
    source_guardrails: dict[str, Any],
    repo_root: Path,
) -> set[str]:
    raw_paths = source_guardrails.get("committed_measurement_guard_paths")
    guard_paths = (
        [Path(str(path)) for path in _sequence(raw_paths, "source_guardrails.committed_measurement_guard_paths")]
        if raw_paths is not None
        else list(DEFAULT_TARGET_BATCH_GUARD_PATHS)
    )
    nuclide_ids: set[str] = set()
    for raw_path in guard_paths:
        path = raw_path if raw_path.is_absolute() else repo_root / raw_path
        if not path.exists():
            raise ValueError(f"Target-batch guard path does not exist: {path}")
        with path.open("r", encoding="utf-8") as handle:
            payload = yaml.safe_load(handle)
        _collect_nuclide_ids(payload, nuclide_ids)
    return nuclide_ids


def _collect_nuclide_ids(payload: Any, output: set[str]) -> None:
    if isinstance(payload, dict):
        if "nuclide_id" in payload:
            output.add(str(payload["nuclide_id"]))
        for value in payload.values():
            _collect_nuclide_ids(value, output)
    elif isinstance(payload, list):
        for value in payload:
            _collect_nuclide_ids(value, output)


def _validate_source_state(config: dict[str, Any]) -> None:
    source_state = _mapping(config.get("source_state"), "source_state")
    if source_state.get("live_external_fetch_allowed") is not False:
        raise ValueError("source_state.live_external_fetch_allowed must be false")
    for required_key in (
        "git_commit",
        "training_data_references",
        "holdout_protocol_references",
        "source_data_state_note",
    ):
        if required_key not in source_state:
            raise ValueError(f"source_state must include {required_key}")


def _validate_quantity(config: dict[str, Any]) -> None:
    quantity = str(config.get("quantity", "mass_excess_mev"))
    if quantity != "mass_excess_mev":
        raise ValueError("nuclear prediction variant factory currently supports only mass_excess_mev")


def _validate_unique_prediction_ids(config: dict[str, Any]) -> None:
    prediction_ids = [
        str(variant["prediction_id"])
        for variant in _sequence(config.get("variants"), "variants")
        if isinstance(variant, dict) and variant.get("prediction_id") is not None
    ]
    duplicates = _duplicates(prediction_ids)
    if duplicates:
        raise ValueError(f"Duplicate requested prediction_id values: {', '.join(duplicates)}")


def _validate_unique_output_paths(paths: list[Path]) -> None:
    duplicates = _duplicates([str(path) for path in paths])
    if duplicates:
        raise ValueError(f"Duplicate draft output filenames: {', '.join(duplicates)}")


def _validate_output_directory(output_dir: Path, repo_root: Path, output_config: dict[str, Any]) -> None:
    registry_dir = (repo_root / "prediction_registry" / "nuclear_masses").resolve()
    resolved_output = output_dir.resolve()
    if _is_relative_to(resolved_output, registry_dir) and not bool(
        output_config.get("allow_prediction_registry_output", False)
    ):
        raise ValueError(
            "Refusing to write draft predictions into prediction_registry/nuclear_masses by default; "
            "write to an explicit scratch directory and copy reviewed selections separately."
        )


def _resolve_output_dir(
    *,
    output_dir: str | Path | None,
    output_config: dict[str, Any],
) -> Path:
    if output_dir is not None:
        return Path(output_dir)
    configured = output_config.get("draft_prediction_dir")
    if configured is None:
        raise ValueError("Writing draft predictions requires output.draft_prediction_dir or --output-dir")
    return Path(str(configured))


def _baseline_model_config(config: dict[str, Any]) -> dict[str, Any]:
    baseline = _mapping(config.get("baseline_model"), "baseline_model")
    for key in ("source_path", "registry_model_id"):
        if key not in baseline:
            raise ValueError(f"baseline_model must include {key}")
    return baseline


def _mapping(payload: Any, label: str) -> dict[str, Any]:
    if not isinstance(payload, dict):
        raise ValueError(f"Expected mapping for {label}")
    return payload


def _sequence(payload: Any, label: str) -> list[Any]:
    if not isinstance(payload, list):
        raise ValueError(f"Expected list for {label}")
    return payload


def _validate_coefficient_name(name: str) -> None:
    if name not in COEFFICIENT_NAMES:
        raise ValueError(f"Unknown semi-empirical coefficient name: {name}")


def _duplicates(values: list[str]) -> list[str]:
    seen: set[str] = set()
    duplicates: list[str] = []
    for value in values:
        if value in seen and value not in duplicates:
            duplicates.append(value)
        seen.add(value)
    return duplicates


def _is_relative_to(path: Path, parent: Path) -> bool:
    try:
        path.relative_to(parent)
    except ValueError:
        return False
    return True
