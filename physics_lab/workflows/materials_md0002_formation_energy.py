"""Workflow adapter for the MD-0002 formation-energy benchmark RESULT-0021.

Regenerates the canonical ``results/EXP-0014/RUN-0001/`` artifact from committed
data via ``physics-lab run`` so the Gate B independent-replay validator can
re-run the same command and compare. Numbers come from the deterministic engine
(:func:`compute_result_metrics`), which reuses the frozen TASK-0703 benchmark;
scope and limitation text are preserved verbatim from the published RESULT-0021.
"""

